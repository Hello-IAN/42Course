#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!*lst)
		*lst = new;
	else
	{
		tmp = *lst;
		while ((*lst)->next != NULL)
		{
			*lst = (*lst)->next;
		}
		(*lst)->next = new;
		*lst = tmp;
	}
}
